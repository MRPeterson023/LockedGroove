<!--start/stop spinning record-->
$(".recordButton").click(function() {
	$(".record").toggleClass("recordSpin");
});

$('.modal-player').on('shown', function () {
  		$(".record").removeClass("recordSpin");
})

$('.modal').on('shown', function () {
  		$(".record").removeClass("recordSpin");
})

$(".recordButton").click(function() {
	$(".recordMobile").toggleClass("recordSpinMobile");
});

$('.modal-player-mobile').on('shown', function () {
  		$(".recordMobile").removeClass("recordSpinMobile");
})

$('.modal-mobile').on('shown', function () {
  		$(".recordMobile").removeClass("recordSpinMobile");
})
